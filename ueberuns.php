<h1>Über uns</h1>
<div style="margin-right: 2em">
<div class="tblhead">&nbsp;</div>
<br>
<p>Die Wurzeln von BLECHOPHON liegen in einem Blechbläserensemble welches im Jahr 2006 für einen Auftritt bei einer Bergmesse gegründet wurde und aus Mitgliedern bestand welche sich bereits aus Ihrer gemeinsamen Zeit beim Jugendorchester in Engerwitzdorf kannten.</p>
<br>
<p>In den Jahren danach wurde die Besetzung dieses Ensembles immer wieder verändert und neue Mitglieder kamen hinzu bis schließlich im Jahr 2008 BLECHOPHON als eigenständiges Ensemble gegründet wurde.</p>
<br>
<p>Seit dieser Zeit spielt BLECHOPHON bei Früh-bzw. Dämmerschoppen, Polterabenden sowie Weihnachts – und Firmenfeiern frei nach unserem Motto „Musik mit Begeisterung und Leidenschaft auf hohem Niveau“.</p>
<br>
<p>Doch nicht nur Unterhaltungsmusik zählt zum Repertoire von BLECHOPHON sondern auch konzertante Blechbläsermusik wie mehrmalige Erfolge beim Wettbewerb Musik in kleinen Gruppen, ein Gemeinschaftskonzert mit dem Wartberger Chor „Pro Musica“ und eigene Konzerte mit dem Titel „Blechophon in Concert“ im Jahr 2014 und „Blechophon XL in Concert“ im Jahr 2016 zeigen.</p>
<br>
<br>
<p><span style="font-weight: bolder">BLECHOPHON</span> immer die richtige Wahl für gute Unterhaltung und Stimmung bei Ihrer Veranstaltung.</p>
</div>